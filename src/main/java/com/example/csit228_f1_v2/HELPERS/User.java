package com.example.csit228_f1_v2.HELPERS;

public class User {
    public int userid;
    public String fname;
    public String lname;
    public String email;
    public String username;
    public String password;

    public User(int userid, String fname, String lname, String email, String username, String password) {
        this.userid = userid;
        this.fname = fname;
        this.lname = lname;
        this.email = email;
        this.username = username;
        this.password = password;
    }
}
